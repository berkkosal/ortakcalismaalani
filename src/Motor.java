public class Motor {


    private double motorHacmi;
    private double beygir;
    private double tork;


    public Motor(double motorHacmi, double beygir, double tork) {

        this.motorHacmi = motorHacmi;
        this.beygir = beygir;
        this.tork = tork;


    }

    public double getMotorHacmi() {
        return motorHacmi;
    }

    public double getBeygir() {
        return beygir;
    }

    public double getTork() {
        return tork;
    }
}
