public class Lastik {


    private int yanakGenisligi;
    private String marka;

    public Lastik(int yanaklar, String marka) {

        this.yanakGenisligi = yanaklar;
        this.marka = marka;

    }


    public int getYanakGenisligi() {
        return yanakGenisligi;
    }

    public String getMarka() {
        return marka;
    }


}
